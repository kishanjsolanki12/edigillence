        function validateForm(event) {
            const mobileNumber = document.getElementById('mobile').value;
            const regex = /^([0-9\s\-\+\(\)]*)$/;
            const errorMessage = document.getElementById('error-message');
            errorMessage.innerText = '';

            if (!mobileNumber) {
                errorMessage.innerText = 'A mobile number is required';
                event.preventDefault();
                return false;
            }

            if (!regex.test(mobileNumber)) {
                errorMessage.innerText = 'Please enter a valid mobile number';
                event.preventDefault();
                return false;
            }

            if (mobileNumber.length < 10) {
                errorMessage.innerText = 'The mobile number must be at least 10 digits';
                event.preventDefault();
                return false;
            }

            if (mobileNumber.length > 15) {
                errorMessage.innerText = 'The mobile number may not be greater than 15 digits';
                event.preventDefault();
                return false;
            }

            return true;
